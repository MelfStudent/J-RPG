namespace J_RPG.Enums
{
    /// <summary>
    /// Enum representing the different types of actions that can be performed by characters in the game.
    /// </summary>
    public enum ActionType
    {
        /// <summary>
        /// Represents an action that deals damage to a target.
        /// </summary>
        Damage,

        /// <summary>
        /// Represents an action that heals a target, restoring their health.
        /// </summary>
        Heal,

        /// <summary>
        /// Represents an action that buffs a target, enhancing their abilities or stats.
        /// </summary>
        Buff,

        /// <summary>
        /// Represents an action that debuffs a target, weakening their abilities or stats.
        /// </summary>
        Debuff
    }
}
