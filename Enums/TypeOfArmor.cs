namespace J_RPG.Enums
{
    /// <summary>
    /// Enum representing the different types of armor in the game.
    /// </summary>
    public enum TypeOfArmor
    {
        /// <summary>
        /// Represents fabric armor, which is typically light and offers minimal protection.
        /// </summary>
        Fabric,

        /// <summary>
        /// Represents leather armor, which provides moderate protection against both physical and magic damage.
        /// </summary>
        Leather,

        /// <summary>
        /// Represents mesh armor, which offers a balance of protection against both physical and magic damage.
        /// </summary>
        Mesh,

        /// <summary>
        /// Represents plate armor, which provides the highest protection against physical damage.
        /// </summary>
        Plates
    }
}
